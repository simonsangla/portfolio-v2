  const SUPPORTED = ['en', 'pt', 'fr'];
  const STORAGE_KEY = 'simonsangla.lang';

  function currentLang() {
    const saved = (localStorage.getItem(STORAGE_KEY) || '').toLowerCase();
    if (SUPPORTED.indexOf(saved) >= 0) return saved;
    const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
    if (SUPPORTED.indexOf(nav) >= 0) return nav;
    return 'en';
  }

  function applyLang(lang) {
    if (SUPPORTED.indexOf(lang) < 0) lang = 'en';
    const dict = DICT[lang] || DICT.en;
    const fallback = DICT.en;

    document.documentElement.setAttribute('lang', lang);

    // Plain text translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = dict[key] != null ? dict[key] : fallback[key];
      if (val != null) el.textContent = val;
    });
    // HTML translations (preserve inline <br>, <strong>, <span class="dim">…)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const val = dict[key] != null ? dict[key] : fallback[key];
      if (val != null) el.innerHTML = val;
    });

    // Toggle active class on lang switch buttons
    document.querySelectorAll('.lang-switch button[data-lang]').forEach(b => {
      b.classList.toggle('on', b.dataset.lang === lang);
    });

    localStorage.setItem(STORAGE_KEY, lang);
  }

  function init() {
    applyLang(currentLang());
    document.querySelectorAll('.lang-switch button[data-lang]').forEach(b => {
      b.addEventListener('click', () => applyLang(b.dataset.lang));
    });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
